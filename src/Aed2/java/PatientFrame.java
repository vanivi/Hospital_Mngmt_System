    public PatientFrame() {
        initComponents();
        ArrayList<String> data=new ArrayList<String>();
        String community="";
        for(String key : Assignment2.patients.keySet()){
            if(Assignment2.patients.get(key).getPatientUserName().equals(Assignment2.currentUser)){
               community=Assignment2.patients.get(key).getCommunityName();
            }
        }
        for(String key : Assignment2.hospitals.keySet()){
            if(Assignment2.hospitals.get(key).getCommunityName().equals(community)){
                data.add(Assignment2.hospitals.get(key).getHospitalName());
            }
        }
        DefaultComboBoxModel model= new DefaultComboBoxModel(data.toArray(new String[0]));
        hosptial.setModel(model);
        DefaultTableModel tbModel=(DefaultTableModel)jTable1.getModel();
        for(String key : Assignment2.encounters.keySet()){
            if(Assignment2.encounters.get(key).getBp()!=0){
                String textToShow="";
                boolean value= Assignment2.encounters.get(key).isNormal(Assignment2.encounters.get(key).getBp(),Assignment2.encounters.get(key).getHeartRate());
                if(value){
                    textToShow="Yes";
                }
                else{
                    textToShow="No";

                }
                String testData[]={Assignment2.encounters.get(key).getEncounterId(),Assignment2.encounters.get(key).getDoctorUserName(),Assignment2.encounters.get(key).getTimeStamp(),Integer.toString(Assignment2.encounters.get(key).getBp()),Integer.toString(Assignment2.encounters.get(key).getHeartRate()),textToShow};
                tbModel.insertRow(tbModel.getRowCount(),testData);
            }
        }
    }

