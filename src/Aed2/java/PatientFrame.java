    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String doctorUserName="";
        for(String key : Assignment2.doctors.keySet()){
            if(Assignment2.doctors.get(key).getDoctorName().equals(doctor.getSelectedItem().toString())){
                doctorUserName=Assignment2.doctors.get(key).getDoctorUserName();
            }
        }
        Assignment2.createEncounter(Assignment2.currentUser, doctorUserName, new SimpleDateFormat("MM/dd HH:mm").format(Calendar.getInstance().getTime()), 0, 0);
        JOptionPane.showMessageDialog(this, "Appointment Booked");
    }//GEN-LAST:event_jButton2ActionPerformed

