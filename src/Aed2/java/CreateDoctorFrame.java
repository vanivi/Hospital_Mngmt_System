    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(doctorTable.getSelectedRowCount()==1){
            DefaultTableModel tbModel=(DefaultTableModel)doctorTable.getModel();
            Assignment2.updateDoctor(doctorId.getText(), doctorName.getText(), hospital.getSelectedItem().toString(), community.getSelectedItem().toString(), city.getSelectedItem().toString(), password.getText());
            tbModel.setValueAt(doctorName.getText(), doctorTable.getSelectedRow(), 0);
            tbModel.setValueAt(hospital.getSelectedItem().toString(), doctorTable.getSelectedRow(), 1);
            tbModel.setValueAt(community.getSelectedItem().toString(), doctorTable.getSelectedRow(), 2);
            tbModel.setValueAt(city.getSelectedItem().toString(), doctorTable.getSelectedRow(), 3);
            doctorId.setText("");
            doctorName.setText("");
            password.setText(""); 
        }
        else{
            if(doctorTable.getRowCount()==0){
               JOptionPane.showMessageDialog(this, "Table is Empty"); 
            }
            else{
                JOptionPane.showMessageDialog(this, "Select a Row");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(doctorTable.getSelectedRowCount()==1){
            DefaultTableModel tbModel=(DefaultTableModel)doctorTable.getModel();
            Assignment2.deleteHospital(doctorId.getText().toString());
            tbModel.removeRow(doctorTable.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Deleted Successfully");
            doctorId.setText("");
            doctorName.setText("");
            password.setText("");
        }
        else{
            if(doctorTable.getRowCount()==0){
               JOptionPane.showMessageDialog(this, "Table is Empty"); 
            }
            else{
                JOptionPane.showMessageDialog(this, "Select a Row");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

