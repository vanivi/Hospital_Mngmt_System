    private void doctorTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_doctorTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tbModel=(DefaultTableModel)doctorTable.getModel();
        doctorName.setText(tbModel.getValueAt(doctorTable.getSelectedRow(), 0).toString());
        hospital.setSelectedItem(tbModel.getValueAt(doctorTable.getSelectedRow(), 1).toString());
        community.setSelectedItem(tbModel.getValueAt(doctorTable.getSelectedRow(), 2).toString());
        city.setSelectedItem(tbModel.getValueAt(doctorTable.getSelectedRow(), 3).toString());
        String doctorIdString="";
        for (String key : Assignment2.doctors.keySet()) {
            if(Assignment2.doctors.get(key).getDoctorName().equals((tbModel.getValueAt(doctorTable.getSelectedRow(), 0).toString()))){
                doctorIdString=Assignment2.doctors.get(key).getDoctorUserName();
                doctorId.setText(doctorIdString);
            }
        }
        for (String key : Assignment2.users.keySet()) {
            if(Assignment2.users.get(key).getUserName().equals(doctorIdString)){
                password.setText(Assignment2.users.get(key).getPassword());
            }
        }
    }//GEN-LAST:event_doctorTableMouseClicked

